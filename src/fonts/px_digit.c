uint8_t** px_digit_get(enum px_digit_size size, char number)
{
    uint8_t **ptr;

    ptr = NULL;
    if (number >= 0 && number <= 9){
        if (PX_DIGIT_SIZE_5x3 == size){
            ptr = (uint8_t**)g_digit5x3[number];
        } else {
            ASSERT(0);
        }
    } else {
        ASSERT(0);
    }

    return ptr;
}


